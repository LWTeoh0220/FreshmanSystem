export function generateMap() {
  // 全部初始化為草地
  const map = Array.from({ length: 90 }, () => new Array(160).fill(0));

  function fill(x, y, w, h, tile) {
    for (let r = y; r < y + h && r < 90; r++)
      for (let c = x; c < x + w && c < 160; c++)
        map[r][c] = tile;
  }

  // === 校外馬路 ===
  fill(0,   0,  160, 3,  1);   // 八德路（北）
  fill(0,  86,  160, 4,  1);   // 忠孝東路（南）
  fill(0,   0,    3, 90, 1);   // 新生南路（西）

  // === 校內磚紅走道 ===
  fill(30,  0, 4, 90, 2);   // 縱向小道1
  fill(58,  0, 4, 90, 2);   // 縱向小道2
  fill(120, 0, 6, 90, 2);   // 縱向小道3

  fill(2,  24, 158, 12, 2); // 橫向主道1 (y=24~36)
  fill(42, 42, 4,   26, 2); // 中左縱向穿插小道
  fill(42, 44, 78,  4,  2); // 橫向小道2 (y=44~48)

  // === 建築（全部填 tile=3）===
  BUILDING_LABELS.forEach(b => {
    fill(b.x, b.y, b.w, b.h, 3);
  });

  return map;
}
